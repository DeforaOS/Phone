#variables
#executables
XSLTPROC="xsltproc"


#functions
#main
clean=0
while getopts "cP:" name; do
	case "$name" in
		c)
			clean=1
			;;
		P)
			#XXX ignored
			;;
		?)
			_usage
			exit $?
			;;
	esac
done
shift $((OPTIND - 1))
if [ $# -ne 1 ]; then
	_usage
	exit $?
fi
target="$1"

[ "$clean" -ne 0 ]						&& exit 0
$XSLTPROC "${target%.*}.xsl" "${target%.*}.xml" > "$target"
